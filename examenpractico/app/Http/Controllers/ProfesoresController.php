     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $profesores = new Profesores();
        $profesores->DNI = $request->get('DNI');
        $profesores->Nombres = $request->get('nombres');
        $profesores->Apellidos = $request->get('apellidos');
        $profesores->Direccion = $request->get('direccion');
        $profesores->Titulo = $request->get('titulo');
        $profesores->Telefono = $request->get('telefono');
       
        $profesores->save();

        return redirect('/profesores');

    }

